#Вычислить число Пи c заданной точностью d
#*Пример:* 
#- при d = 0.001, π = 3.141
#- при d = 0.0001, π = 3.1415

import math

# Получаем число Пи
pi = math.pi

# Вводим требуемую точность
d = float(input("Введите точность (например, 0.001): "))

# Определяем количество знаков после запятой
count = abs(int(math.log10(d))) + 1

# Округляем число Пи до нужной точности
rounded_pi = round(pi, count)

# Выводим результат
print(f"Пи с точностью до {d}: {rounded_pi}")