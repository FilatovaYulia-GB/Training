#Задайте список из N элементов, заполненных числами из промежутка [-N, N].
# Найдите произведение элементов на указанных позициях.
# Позиции хранятся в файле file.txt в одной строке одно число.
# (для продвинутых - с файлом, вариант минимум - ввести позиции в консоли)
#-2 -1 0 1 2
#Позиции: 0,1 -> 2

import random

# Запрашиваем значение N
N = int(input('Введите N: '))

# Генерация списка из N элементов в диапазоне [-N, N]
numbers = [random.randint(-N, N) for _ in range(N)]
print('Список:', numbers)

# Запрашиваем две позиции для умножения
pos1 = int(input('Введите первую позицию: '))
pos2 = int(input('Введите вторую позицию: '))

# Проверяем, находятся ли указанные позиции внутри диапазона
if pos1 > len(numbers) or pos2 > len(numbers) or pos1 <= 0 or pos2 <= 0:
    print('Некорректная позиция!')
else:
    # Умножаем элементы на указанных позициях (индексация с 1)
    product = numbers[pos1 - 1] * numbers[pos2 - 1]
    print(f'Произведение элементов: {numbers[pos1 - 1]} * {numbers[pos2 - 1]} = {product}')