#Напишите программу, которая найдёт произведение пар чисел списка.
#Парой считаем первый и последний элемент, второй и предпоследний и т.д.
#*Пример:*
#- [2, 3, 4, 5, 6] => [12, 15, 16];
#- [2, 3, 5, 6] => [12, 15]

from random import randint

# Ввод размера списка
number = int(input('Введите размер списка: '))

# Генерация списка случайных чисел
mass = [randint(0, 9) for _ in range(number)]

# Результат произведения пар
mass2 = []

# Проходим по половине списка и находим произведения пар
for i in range(len(mass) // 2):
    mass2.append(mass[i] * mass[-(i + 1)])

# Если длина списка нечётная, добавляем центральный элемент
if len(mass) % 2 == 1:
    mass2.append(mass[len(mass) // 2])

# Выводим исходный список и результат
print(mass)
print(mass2)